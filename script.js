function viewDetails1() {
    var vd = document.getElementById("view-details-1");
    if (vd.style.display === "none") {
      vd.style.display = "block";
      document.getElementById("view-details-button-1").innerHTML = "Show Less";
    } else {
      vd.style.display = "none";
      document.getElementById("view-details-button-1").innerHTML = "View Details";
    }
}
